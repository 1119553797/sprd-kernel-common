{
	uint32_t i = 0;
	int ret = 0;
	SENSOR_EXT_FUN_PARAM_T_PTR ext_ptr = (SENSOR_EXT_FUN_PARAM_T_PTR)param;
	SENSOR_REG_T_PTR reg_ptr = ov5640_init_single_firmware;

	printk("_ov5640_init_firmware: cmd=%d!.\n",ext_ptr->cmd);		
	switch(ext_ptr->param)
	{
		case SENSOR_EXT_FOCUS_TRIG://auto focus
			reg_ptr = ov5640_init_single_firmware;
			break;
		default:
			break;
	}
	for(i=0x00; (0xffff!=reg_ptr[i].reg_addr)||(0xFF!=reg_ptr[i].reg_value); i++)
	{
		ret = Sensor_WriteReg(reg_ptr[i].reg_addr, reg_ptr[i].reg_value);
		if(ret != 0)
		{
			printk("_ov5640_init_firmware: write sensor error!.\n");		
			break;
		}
			
	}
	printk("_ov5640_init_firmware: E!.\n");		
	return ret;		
}
